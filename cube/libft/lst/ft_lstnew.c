#include "libft.h"

t_list	*ft_lstnew(void *content)
{
	struct s_list	*list;

	list = (struct s_list *)malloc(sizeof(struct s_list));
	if (list == NULL)
		return (0);
	list->content = content;
	list->next = NULL;
	return (list);
}
