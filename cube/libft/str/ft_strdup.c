#include <stdlib.h>

char	*ft_strdup(const char *s1)
{
	int		i;
	char	*buffer;

	if (s1 == NULL)
		return (NULL);
	i = 0;
	while (s1[i] != '\0')
		i++;
	buffer = (char *)malloc(i + 1);
	if (buffer == NULL)
		return (0);
	i = 0;
	while (s1[i] != '\0')
	{
		buffer[i] = s1[i];
		i++;
	}
	buffer[i] = '\0';
	return (buffer);
}
